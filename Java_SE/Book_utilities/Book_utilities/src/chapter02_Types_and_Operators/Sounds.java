package chapter02_Types_and_Operators;

public class Sounds {
	// calculate distance between an object and a thunder
	
	public static void main(String[] args) {
		final double THUNDER = 1100;
		final double TIME = 7.2;
		
		final double DISTANCE = TIME * THUNDER;
		System.out.println("Thunder is " + DISTANCE + " foots away"); 
		
	}
}
